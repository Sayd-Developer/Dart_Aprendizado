main() {
// Operadores Aritméticos

  double a = 2;
  a = a + 3;
  a += 3;
  a -= 3;
  a *= 3;
  a /= 3;
  a %= 3;

  print(a);

  // Operadores Relacionais (Binário/Infix) ---> O resultado sempre é bool.

  print(3 > 2);
  print(3 >= 3);
  print(3 < 4);
  print(3 <= 3);
  print(3 != 3);
  print(3 == 3);
  print(3 == '3');

  print(2 + 5 > 3 - 2 && 4 + 7 != 78 - 2);

  // 101 = 5
  // 100 = 5
  // 100 = 5

  print(5 & 4);
}
